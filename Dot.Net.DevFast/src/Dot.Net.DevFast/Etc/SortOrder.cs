namespace Dot.Net.DevFast.Etc
{
    /// <summary>
    /// Enumeration related to sorting requirements.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// Represents no sorting.
        /// </summary>
        None=0,
        /// <summary>
        /// Ascending (lowest to highest) sorting order.
        /// </summary>
        Asc,
        /// <summary>
        /// Descending (highest to lowest) sorting order
        /// </summary>
        Desc
    }
}